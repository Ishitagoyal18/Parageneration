// Function to generate a paragraph based on selected interests and hobbies
function generateParagraph(name, selectedInterests, selectedHobbies) {
  let paragraph = ""; // Initialize paragraph
  const nameIntroLine = nameIntroLines[Math.floor(Math.random() * nameIntroLines.length)];
  // Add main introductory line for the paragraph
  const mainIntroLine = mainIntroLines[Math.floor(Math.random() * mainIntroLines.length)];
  paragraph += `${nameIntroLine} ${name}. ${mainIntroLine}`;

  // Generate lines for each interest
  selectedInterests.forEach((interest, index) => {
    let introLine;
    if (index === 0) {
      const randomIndex = Math.floor(Math.random() * firstInterestIntroLines.length);
      introLine = firstInterestIntroLines[randomIndex];
    } else {
      const randomIndex = Math.floor(Math.random() * subsequentInterestIntroLines.length);
      introLine = subsequentInterestIntroLines[randomIndex];
    }

    // Add introductory line for the interest
    paragraph += ` ${introLine} ${interest.toLowerCase()}.`;

    // Add a line about the interest
    const lines = interestLines[interest];
    if (lines) {
      const randomIndex = Math.floor(Math.random() * lines.length);
      paragraph += ` ${lines[randomIndex]}`; // No period here
    } else {
      paragraph += ` No information available for ${interest}.`; // Handle invalid interests
    }

    // Add separator between interests and hobbies
    if (index < selectedInterests.length - 1) {
      paragraph += " ";
    }
  });

  // Add introductory line for hobbies
  if (selectedHobbies.length > 0) {
    const hobbyIntroLine = hobbyIntroLines[Math.floor(Math.random() * hobbyIntroLines.length)];
    paragraph += ` ${hobbyIntroLine} `;
  }

  // Generate lines for hobbies
  selectedHobbies.forEach((hobby, index) => {
    paragraph += `${hobby.toLowerCase()}`;

    // Add comma between hobbies
    if (index < selectedHobbies.length - 2) {
      paragraph += ", ";
    }

    // Add "and" before the last hobby
    if (index === selectedHobbies.length - 2) {
      paragraph += " and ";
    }
  });

  // Add a full stop at the end
  paragraph += ".";

  return paragraph; // No trimming here
}
module.exports = generateParagraph;
